/** Build a ChecklistTemplate for a given work order type and language */
export function buildProcedureTemplate(type: WorkOrderType, language: Language = "en"): ChecklistTemplate {
  const strings = PROCEDURE_TRANSLATIONS[language] || PROCEDURE_TRANSLATIONS.en;
  const itemDefs = PROCEDURE_ITEMS[type];

  return {
    id: WORK_ORDER_PROCEDURE_TEMPLATE_IDS[type],
    company_id: "__built_in__",
    name: strings.name[type],
    description: strings.description[type],
    category: type === "emergency" ? "emergency" : type === "inspection" ? "inspection" : type === "service_request" ? "service" : "maintenance",
    work_order_type: type,
    assignment: "all",
    recurrence: "once",
    items: itemDefs.map((item, index) => ({
      id: item.id,
      question: strings.questions[item.key] || PROCEDURE_TRANSLATIONS.en.questions[item.key] || item.key,
      type: item.type,
      required: item.required,
      order: index + 1,
    })),
    tags: [],
    publish_status: "published",
    is_active: true,
    created_at: now,
    updated_at: now,
  };
}

/** Pre-built English templates (used as initialData for the store) */
export const WORK_ORDER_PROCEDURE_TEMPLATES: ChecklistTemplate[] = [
  buildProcedureTemplate("preventive_maintenance"),
  buildProcedureTemplate("corrective_maintenance"),
  buildProcedureTemplate("emergency"),
  buildProcedureTemplate("inspection"),
  buildProcedureTemplate("service_request"),
];

/** Get the built-in procedure template for a work order type */
export function getProcedureTemplateForType(type: WorkOrderType): ChecklistTemplate {
  const id = WORK_ORDER_PROCEDURE_TEMPLATE_IDS[type];
  return WORK_ORDER_PROCEDURE_TEMPLATES.find((t) => t.id === id)!;
}

/** Get the built-in template ID for a work order type */
export function getProcedureTemplateIdForType(type: WorkOrderType): string {
  return WORK_ORDER_PROCEDURE_TEMPLATE_IDS[type];
}
