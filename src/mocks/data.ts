// User stats - computed from mock data
export function getUserStats(userId: string) {
  const userIncidents = getStoredIncidents().filter((i) => i.reporter_id === userId);
  const userTickets = getStoredTickets().filter((t) => t.assigned_to === userId);
  const completedTickets = userTickets.filter((t) => t.status === "resolved" || t.status === "closed");
  
  // Calculate "safe days" - days since last incident reported by user
  const lastIncidentDate = userIncidents.length > 0 
    ? new Date(Math.max(...userIncidents.map(i => new Date(i.incident_date).getTime())))
    : new Date(Date.now() - 30 * 24 * 60 * 60 * 1000); // 30 days ago if no incidents
  const safeDays = Math.floor((Date.now() - lastIncidentDate.getTime()) / (1000 * 60 * 60 * 24));
  
  // Tasks completed this week
  const oneWeekAgo = new Date(Date.now() - 7 * 24 * 60 * 60 * 1000);
  const tasksCompletedThisWeek = completedTickets.filter(
    (t) => t.updated_at && new Date(t.updated_at) > oneWeekAgo
  ).length;

  return {
    totalIncidentsReported: userIncidents.length,
    openIncidents: userIncidents.filter((i) => i.status === "new" || i.status === "in_progress").length,
    assignedTasks: userTickets.filter((t) => t.status === "new" || t.status === "in_progress").length,
    completedTasks: completedTickets.length,
    safeDays: Math.max(safeDays, 0),
    tasksCompletedThisWeek: tasksCompletedThisWeek,
  };
}

